<p dir="ltr"><span style="font-size: 14pt;"><strong>Our Discourses</strong></span></p>
<p dir="ltr">1. Most tech hubs and user-groups all over the world are concentrated in highly modernised and developed locations of the world like Silicon Valley. We are trying to spark and strengthen tech communities away from the cities. In the counties in Kenya and the suburbs off the world.</p>
<p dir="ltr">2. Collaboration plays a huge role in influencing innovation. Good ideas need a team and partners and exposure to critics and feedback in order to germinate successfully. Inability for developers to discover each other and connect physically locally in the developing locations of the world is a big hindrance to this development. We need more information exchange, networking, coworking and peer programming.</p>
<p dir="ltr">3. Traditionally, software development was typically done by people who are reserved and it was unhealthy. We are trying to make it a communal and fun activity of the 21st century. So &nbsp;we are facilitating the tribe of social developers with a tool to connect and cowork.</p>
